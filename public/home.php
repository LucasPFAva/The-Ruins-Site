<h1 id="header">The Ruins</h1>
<h2>Introduction</h2>
<p>We are a team of two aspiring indie-developers (<a href="https://github.com/LucasPFAva" target="_blank">Loamo</a> and <a href="https://github.com/Thecutedragon" target="_blank">The Cute Dragon</a>) who have set out on a mission to create an MMORPG game set in a Ruin, hence the name.</p>
<h2>Inspiration</h2>
<h3>The Binding of Issac</h3>
<p>The Binding of Isaac inspired the overall environment where the game will take place, a Ruin. In this Ruin the player will be able to find loot in the rooms one enters.</p>
<figure>
    <img src="<?=base()?>images/The-Binding-of-Issac.jpg" alt="An image of the game, The Binding of Issac.">
    <figcaption>Fig.1 - The offical release cover for The Binding of Issac Afterbirth+.</figcaption>
</figure>
<h3>Diablo III</h3>
<p>Diablo III inspired the games progression system, whereby loot is determined by the difficulty level set by the player. In the beginning, a higher difficulty will be near to impossible but after grinding, difficulty levels will be swapped quite regularly.</p>
<figure>
    <img src="<?=base()?>images/Diablo-III.png" alt="An image of the game, Diablo III">
    <figcaption>Fig.2 - All the Diablo III difficulty levels.</figcaption>
</figure>
<p>Rare Item drops will increase in an acending order for each difficulty level.</p>
