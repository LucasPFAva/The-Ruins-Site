<h1>The Ruins</h1>
<p>We are a team of two aspiring indie-developers (<a href="https://github.com/LucasPFAva" target="_blank">Loamo</a> and <a href="https://github.com/Thecutedragon" target="_blank">the Cute Dragon</a>) who have set out on a mission to create a game to the likes of "The Binding of Issac". But in the format of an MMORPG.</p>
<p>This page will keep you updated on the latest news surrounding our development, a wiki managed by us, in addition to hosting the games launcher.</p>
<p>The game will be available early next year (2022).</p>
<a href="https://www.github.com/LucasPFAva/The-Ruins" target="_blank">GitHub Page</a>

<h2>Planned Features</h2>
<p>* A Campaign and Adventure mode (Unlocked after beating Campaign)</p>
<p>* Support for locally hosted servers and private standalone servers (Offical EU server will be hosted by us)</p>
<p>* Language support: English, Swedish (Additional languages will be available through langauge packages downloaded on the offical website or directly in the game launcher).</p>
<p>* SFX:s and OST:s will be composed by us.</p>
<p>* Cross Platform support. The game will be available for download on Windows, Linux and MacOS respectively.</p>
<p>* An In-Game chat. For chatting with friends and mates (There will exist an optional profanity filter).</p>
<p>* Ultrawide and super ultrawide aspect ratios(21:9, 32:9) will be natively supported.</p>
<p>* The games visuals will take inspartion from pixelart.</p>
<p>* Full controller support. The menus and gameplay will be completely usable without the interference of a keyboard and mouse.</p>